    public String[] getOptionSurat() {
        SuratPengantar[] sp = new SuratPengantar[8];
        sp[0] = new SuratPengantarDomisili();
        sp[1] = new SuratPengantarKK();
        sp[2] = new SuratPengantarKTP();
        sp[3] = new SuratPengantarKelahiran();
        sp[4] = new SuratPengantarKematian();
        sp[5] = new SuratPengantarMutasi();
        sp[6] = new SuratPengantarNikah();
        sp[7] = new SuratPengantarUsaha();

        String st[];
        String surat = "-,";
        for (int i = 0; i < sp.length; i++) {
            surat = surat + sp[i].tampilNamaSurat() + ",";
        }
        st = surat.split(",");
         
        return st;
    }

    public void insertDataDashboard() {
        dataSurat = new SuratPengantar(status, link);
        try {
            database = new SmartDatabase();
            conn = Koneksi.koneksi();
            pst = conn.prepareStatement(database.insertDashboard(dataSurat));
            pst.execute();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void insertJenisSurat() {
        //melakukan set data inputan dari combo box dan edit text
        dataSurat.setNamaSurat(cbSuratPengantar.getSelectedItem().toString().trim());
        

        try {
            database = new SmartDatabase();
            conn = Koneksi.koneksi(); 
            pst = conn.prepareStatement(database.insertJenisSurat(dataSurat.getNamaSurat()));
            pst.execute();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void insertSuratPengantar() {
        /*inisialisasi objek supaya format tanggal yang di inputkan 
        menjadi sesuai dengan format tanggal di sql*/
        java.sql.Date date = new Date(edtTanggal.getDate().getTime());
        //melakukan set data inputan dari edit text
        dataSurat.setTanggalSurat(date.toString());
        dataSurat.setKeperluan(edtKeperluan.getText().toString().trim());
        dataSurat.setNik(edtNIK.getText().toString().trim());

        dataSurat = new SuratPengantar(dataSurat.getTanggalSurat(), 
                    dataSurat.getKeperluan(), dataSurat.getNik());

        try {
            database = new SmartDatabase();
            conn = Koneksi.koneksi();
            pst = conn.prepareStatement(database.insertSuratPengantar(dataSurat));
            pst.execute();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

