    private void btnAjukanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAjukanMouseClicked

        //melakukan dialog konfirmasi
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah data surat sudah benar ?");

        switch (confirm) {
            case JOptionPane.YES_OPTION:
                //pemanggilan method
                insertDataDashboard();
                insertJenisSurat();
                insertSuratPengantar();
                //menampilkan frame DashboardUser
                new DashboardUser(dataUser.getPassword()).setVisible(true);
                this.setVisible(false);
                JOptionPane.showMessageDialog(this, "Surat Pengantar berhasil diajukan");
            case JOptionPane.NO_OPTION:
                break;
        }
    }//GEN-LAST:event_btnAjukanMouseClicked

